def derive_high_risk_table() -> None:
    srcs = [
        OUTPUTS / "models" / "formal_graph_models_A_B_s0_4_e50_summary.csv",
        OUTPUTS / "models" / "gcn_stabilized_teg_s0_4_e50_summary.csv",
    ]
    rows: list[dict[str, str]] = []
    wanted = {
        "model",
        "split",
        "eval_split",
        "runs",
        "recall_6_8_mean",
        "recall_6_8_std",
        "precision_6_8_mean",
        "precision_6_8_std",
        "pr_auc_high_mean",
        "pr_auc_high_std",
        "high_fn_rate_mean",
        "high_fn_rate_std",
        "mae_mean",
        "qwk_mean",
    }
    for src in srcs:
        if not src.exists():
            continue
        for row in read_csv_rows(src):
            out = {key: row.get(key, "") for key in wanted}
            out["source_file"] = rel(src)
            rows.append(out)

    if not rows:
        missing.append({"section": "02_model_results", "expected": "high_risk_metrics.csv", "description": "Could not derive high-risk metrics."})
        return

    fields = [
        "model",
        "split",
        "eval_split",
        "runs",
        "recall_6_8_mean",
        "recall_6_8_std",
        "precision_6_8_mean",
        "precision_6_8_std",
        "pr_auc_high_mean",
        "pr_auc_high_std",
        "high_fn_rate_mean",
        "high_fn_rate_std",
        "mae_mean",
        "qwk_mean",
        "source_file",
    ]
    path = PAPER / "02_model_results" / "high_risk_metrics.csv"
    write_csv(path, rows, fields)
    add_generated(path, "02_model_results", "Combined high-risk recall, precision, PR-AUC, and false-negative metrics.")


def split_beta_by_customer_set() -> None:
    src = FINAL / "tables" / "fig5_pyvrp_beta_curve_data.csv"
    if not src.exists():
        return

    rows = read_csv_rows(src)
    by_set: dict[str, list[dict[str, str]]] = {}
    for row in rows:
        customer_set = row.get("customer_set", "").strip()
        if customer_set:
            by_set.setdefault(customer_set, []).append(row)

    if not by_set:
        missing.append({"section": "05_pyvrp", "expected": "customer_set column in fig5 data", "description": "Could not split beta sensitivity by Set A/B."})
        return

    fields = list(rows[0].keys())
    for customer_set, set_rows in sorted(by_set.items()):
        name = f"beta_sensitivity_set{customer_set}.csv"
        path = PAPER / "05_pyvrp" / name
        write_csv(path, set_rows, fields)
        add_generated(path, "05_pyvrp", f"PyVRP beta sensitivity rows for customer set {customer_set}.")


