PAPER = FINAL / "paper_results"


@dataclass
class ManifestRow:
    section: str
    filename: str
    source: str
    description: str


manifest: list[ManifestRow] = []
missing: list[dict[str, str]] = []


def ensure_dir(path: Path) -> Path:
    path.mkdir(parents=True, exist_ok=True)
    return path


def rel(path: Path) -> str:
    try:
        return str(path.relative_to(ROOT))
    except ValueError:
        return str(path)


def copy_file(src: Path, section: str, name: str | None = None, desc: str = "", required: bool = False) -> Path | None:
    if not src.exists():
        row = {"section": section, "expected": rel(src), "description": desc}
        missing.append(row)
        if required:
            print(f"[missing required] {src}")
        return None

    dst_dir = ensure_dir(PAPER / section)
    dst = dst_dir / (name or src.name)
    shutil.copy2(src, dst)
    manifest.append(ManifestRow(section, dst.name, rel(src), desc))
    return dst


def copy_tree_files(src_dir: Path, section: str, suffixes: set[str], desc: str) -> None:
    if not src_dir.exists():
        missing.append({"section": section, "expected": rel(src_dir), "description": desc})
        return

    dst_dir = ensure_dir(PAPER / section)
    for src in sorted(p for p in src_dir.iterdir() if p.is_file() and p.suffix.lower() in suffixes):
        dst = dst_dir / src.name
        shutil.copy2(src, dst)
        manifest.append(ManifestRow(section, dst.name, rel(src), desc))


def read_csv_rows(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8-sig", newline="") as fh:
        return list(csv.DictReader(fh))


def write_csv(path: Path, rows: list[dict[str, object]], fieldnames: list[str]) -> None:
    ensure_dir(path.parent)
    with path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def add_generated(path: Path, section: str, desc: str) -> None:
    manifest.append(ManifestRow(section, path.name, "generated by organize_paper_results.py", desc))


