def write_manifest() -> None:
    manifest_path = PAPER / "00_manifest" / "manifest.csv"
    ensure_dir(manifest_path.parent)
    with manifest_path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=["section", "filename", "source", "description"])
        writer.writeheader()
        for row in manifest:
            writer.writerow(row.__dict__)

    missing_path = PAPER / "00_manifest" / "missing_expected_files.csv"
    fields = ["section", "expected", "description"]
    with missing_path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fields)
        writer.writeheader()
        for row in missing:
            writer.writerow(row)

    readme = PAPER / "README.md"
    readme.write_text(
        "\n".join(
            [
                "# Stable-Tail GNN paper results package",
                "",
                "This folder collects the table data, figure data, and supporting raw experiment outputs used for the final Stable-Tail GNN results.",
                "",
                "Folder layout:",
                "",
                "- `01_data_statistics`: data audit, graph statistics, label distribution, and edge-attribute distribution.",
                "- `02_model_results`: main model metrics, Stable-Tail ablation, and high-risk metrics.",
                "- `03_risk_matrix`: final risk-matrix exports, floor diagnostics, tail metrics, top nodes, and top edges.",
                "- `04_od_validation`: OD path validation results and representative OD case data.",
                "- `05_pyvrp`: PyVRP beta sensitivity, model comparison, common evaluator, and VRP case data.",
                "- `06_concentration`: concentration-aware lambda results and edge-burden support tables.",
                "- `07_figures`: final figure files in PNG and SVG.",
                "- `08_all_final_figure_tables`: all source CSV/JSON tables used by the final figure scripts.",
                "- `00_manifest`: source traceability and missing-file report.",
                "",
                "Primary configuration:",
                "",
                "- Final model: GCN+TEG concat / Stable-Tail GNN.",
                "- Main edge-risk matrix: `w_floor = 0.01 + 0.99w`, `R_ij = w_floor * max(S_i_norm, S_j_norm)`.",
                "- Main OD method: CVaR-risk with alpha=0.9 and k=50.",
                "- Concentration extension: lambda=1 with P75 threshold where applicable.",
                "",
                "Generated by `hazmat_risk_experiments/scripts/organize_paper_results.py`.",
                "",
            ]
        ),
        encoding="utf-8",
    )


def main() -> None:
    ensure_dir(PAPER)
    copy_expected_results()
    write_manifest()
    print(f"Paper results folder: {PAPER}")
    print(f"Copied/generated files: {len(manifest)}")
    print(f"Missing expected files: {len(missing)}")


if __name__ == "__main__":
    main()
